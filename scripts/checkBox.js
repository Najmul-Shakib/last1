document.getElementById('btn-completed-1').addEventListener('click', function(){

    // console.log('hello');
    const val = document.getElementById("check-box").innerText;
    console.log(val);
    const newVal = parseInt(val);
    const valInput = newVal + 1;
    console.log(valInput);
    document.getElementById('check-box').innerHTML = `<p>${valInput}</p>`;

})
